using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace MolarMass_GroupLabB
{
    /// <summary>
    /// Chemical class for storing needed display information
    /// </summary>
    class Chemical
    {
        private int _atomicNum;
        public int atomicNum { get { return _atomicNum; } }     // atomic number property access

        private string _chemicalName;
        public string chemicalName { get { return _chemicalName; } } // chemical name property access

        private string _chemicalSymbol;
        public string chemicalSymbol { get { return _chemicalSymbol; } } // chemical symbol property access

        private double _chemicalMass;
        public double chemicalMass { get { return _chemicalMass; } } // chemical mass property access


        public Chemical ( int aNum, string cName, string cSym, double cMass)
        {
            _atomicNum = aNum;
            _chemicalName = cName;
            _chemicalSymbol = cSym;
            _chemicalMass = cMass;
        }



    }
}
